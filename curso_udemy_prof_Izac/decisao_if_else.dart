void main() {
  var valor = false;

  if (valor == true) {
    print(
        'O valor é $valor'); //$(CIFRÃO) seguido do nome da variável uso pra concatenar STRINGS + VARIÁVEIS.
  } else if (valor == false) {
    //else if É O SENÃO SE que usamos no Python como ELIF.
    print('O valor é $valor');
  } else {
    print('O VALOR NÃO É NENHUM DOS DOIS');
  }
}
